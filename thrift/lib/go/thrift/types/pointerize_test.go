package types

import (
	"testing"

	"github.com/stretchr/testify/require"
)

func TestPointerize(t *testing.T) {
	testInputs := []any{
		bool(true),
		byte(128),
		int(1),
		int8(2),
		int16(3),
		int32(4),
		int64(5),
		uint(6),
		uint8(7),
		uint16(8),
		uint32(9),
		uint64(10),
		float32(123.4),
		float64(567.8),
		string("hello"),
	}

	for _, testInput := range testInputs {
		testInputPtr := Pointerize(testInput)
		require.Equal(t, testInput, *testInputPtr)
	}
}
